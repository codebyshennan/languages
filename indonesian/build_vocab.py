# Regex: reject if it looks like a number, abbreviation (all caps 2-4 chars), or has digits
RE_NUMBER = re.compile(r'^\d+$')
RE_ABBREV = re.compile(r'^[A-Z]{2,4}$')
RE_HAS_DIGIT = re.compile(r'\d')


def is_valid_candidate(word: str) -> bool:
    """Return True if the word is a plausible Indonesian vocabulary word."""
    w = word.strip()
    if not w:
        return False
    # Single character
    if len(w) <= 1:
        return False
    # Pure number
    if RE_NUMBER.match(w):
        return False
    # Contains digits
    if RE_HAS_DIGIT.search(w):
        return False
    # All-caps abbreviation
    if RE_ABBREV.match(w):
        return False
    # In filter set
    if w.lower() in FILTER_SET:
        return False
    # Looks like a URL or has special chars (except hyphen and space)
    if any(c in w for c in ['@', '#', '/', '\\', '.', '(', ')', '[', ']', '"', "'"]):
        return False
    return True


def tokenize_indonesian_cell(cell_value: str) -> set:
    """
    Split an Indonesian column cell on '/' and spaces, returning a set of
    lowercased tokens. Used for duplicate detection.
    """
    if not cell_value:
        return set()
    tokens = re.split(r'[/\s]+', str(cell_value))
    return {t.strip().lower() for t in tokens if t.strip()}


