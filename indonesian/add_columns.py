def get_category(english_word):
    if not english_word or english_word == "— (to be added)":
        return "—"
    w = english_word.lower()
    for keywords, cat in CATEGORY_RULES:
        for kw in keywords:
            if kw.lower() in w:
                return cat
    return "General"


