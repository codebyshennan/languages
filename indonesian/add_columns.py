def normalize_key(english_col):
    """Strip parentheticals and take the first option to make a lookup key."""
    s = re.sub(r'\(.*?\)', '', english_col).strip().strip('/')
    return s.split('/')[0].strip().lower()


def make_english_example(english_col, category="General"):
    """Return a natural English example sentence for the given English word/phrase."""
    if not english_col or english_col == "— (to be added)":
        return "—"

    e = english_col.strip()

    # 1. Exact match in EXAMPLES dict
    if e in EXAMPLES:
        return EXAMPLES[e]

    # 2. Normalized match
    key = normalize_key(e)
    if key in EXAMPLES:
        return EXAMPLES[key]

    # 3. Partial match: check if any EXAMPLES key is a substring of the column value
    e_lower = e.lower()
    for k, ex in EXAMPLES.items():
        if k in e_lower and len(k) > 4:
            return ex

    # ── Category-aware fallback templates ──────────────────────────────
    core = re.sub(r'\(.*?\)', '', e).strip().strip('/')
    core = core.split('/')[0].strip()
    w = core.lower()

    if w.startswith("to "):
        verb = w[3:].strip().split()[0]  # first word of verb phrase
        templates = {
            "Verbs – Movement & Action": f"She decided to {verb} right away.",
            "Verbs – Communication & Mind": f"He tried to {verb} the situation clearly.",
            "Verbs – General": f"We need to {verb} before it's too late.",
        }
        return templates.get(category, f"I try to {verb} every day.")

    if category == "Adjectives":
        return f"The result was surprisingly {w}."

    if category == "Adverbs & Degree Words":
        return f"She completed the task {w} well."

    if category == "Conjunctions & Connectors":
        return f"It was hard, {w} she kept going."

    if category == "Prepositions":
        return f"Put the book {w} the shelf."

    if category == "Numbers & Quantities":
        return f"There are {w} options to consider."

    if category == "Time & Frequency":
        return f"I usually do this {w}."

    if category == "Colors":
        col = w.split()[0]
        return f"The sky turned a deep shade of {col}."

    if category == "Expressions & Phrases":
        return f'"{core}" is a common phrase used in everyday conversation.'

    if category in ("Pronouns & Determiners",):
        return f"Use '{core}' to refer to the subject."

    # Generic fallback — use the word in a simple sentence
    if len(core.split()) <= 2 and not any(c in core for c in ['/', '(', ')']):
        return f"Understanding '{core}' is useful in everyday conversation."
    return f"This concept — '{core}' — is commonly encountered in daily life."


